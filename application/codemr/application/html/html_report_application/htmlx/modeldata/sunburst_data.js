var EQ_CLASS_METRICS = ["C3","Complexity","Coupling","Lack of Cohesion","Size","Class Lines of Code","Weighted Method Count","Degree","OutDegree","InDegree","Number of Fields","Depth of Inheritance Tree","Response For a Class","Lack of Tight Class Cohesion","Lack of Cohesion of Methods","Number of Static Fields","Lack of Cohesion Among Methods(1-CAM)","Simple Response For a Class","Number of Static Methods","CBO Lib","Coupling Between Object Classes","Access to Foreign Data","Specialization Index","Class-Methods Lines of Code","Number of Children","Number of Methods","CBO App","Number of Overridden Methods"];
